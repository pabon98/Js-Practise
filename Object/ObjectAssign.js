// to add a property in object we can use dot notation
const obj = {};
obj.name = "Bike";
obj.age = 2;
obj.speed = "145kmh";
console.log(obj);

// we can also use square bracket notation
const Person = {};
const name = "name";
const age = "age";
const height = "height"

Person[name] = "Hasin Anjum"
Person[age] = 25,
Person[height] = "5'5'"
console.log(Person)
